package id.co.roxas.common.lib.helper;

public class Edge {
	private String labelX;
	private String labelY;
	
	

	public Edge(String labelX, String labelY) {
		this.labelX = labelX;
		this.labelY = labelY;
	}

	public String getLabelX() {
		return labelX;
	}

	public String getLabelY() {
		return labelY;
	}
	


}